package dev.z8emu.machine.apple2.disk;

final class Apple2GcrEncoding {
    static final int[] GCR_6_AND_2 = {
            0x96, 0x97, 0x9A, 0x9B, 0x9D, 0x9E, 0x9F, 0xA6,
            0xA7, 0xAB, 0xAC, 0xAD, 0xAE, 0xAF, 0xB2, 0xB3,
            0xB4, 0xB5, 0xB6, 0xB7, 0xB9, 0xBA, 0xBB, 0xBC,
            0xBD, 0xBE, 0xBF, 0xCB, 0xCD, 0xCE, 0xCF, 0xD3,
            0xD6, 0xD7, 0xD9, 0xDA, 0xDB, 0xDC, 0xDD, 0xDE,
            0xDF, 0xE5, 0xE6, 0xE7, 0xE9, 0xEA, 0xEB, 0xEC,
            0xED, 0xEE, 0xEF, 0xF2, 0xF3, 0xF4, 0xF5, 0xF6,
            0xF7, 0xF9, 0xFA, 0xFB, 0xFC, 0xFD, 0xFE, 0xFF
    };

    private Apple2GcrEncoding() {
    }

    static int encode6And2(int value) {
        return GCR_6_AND_2[value & 0x3F];
    }

    static int encodeMacTriple(int a, int b, int c) {
        int va = a & 0xFF;
        int vb = b & 0xFF;
        int vc = c & 0xFF;
        return (encode6And2(((va >>> 2) & 0x30) | ((vb >>> 4) & 0x0C) | ((vc >>> 6) & 0x03)) << 24)
                | (encode6And2(va) << 16)
                | (encode6And2(vb) << 8)
                | encode6And2(vc);
    }
}
